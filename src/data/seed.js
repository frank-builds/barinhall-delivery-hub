function makeSteps(serviceKey, doneCount = 0) {
  return DEFAULT_WORKFLOWS[serviceKey].map((label, i) => ({
    id: `${serviceKey}-step-${i + 1}`,
    label,
    done: i < doneCount,
  }));
}

